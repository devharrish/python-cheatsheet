'''

Comparison operators are used to compare two values. They return either True or False depending on whether the comparison is correct. It is also called Relational Operators.

== : Equal to
!= : Not equal to
> : Greater than
< : Less than
>= : Greater than or equal to
<= : Less than or equal to

'''

#Equal to

print(2 == 3)  # Output: False

#Not equal to

print(2 != 3)  # Output: True       

#Greater than

print(2 > 3)  # Output: False

#Less than

print(2 < 3)  # Output: True

#Greater than or equal to

print(2 >= 3)  # Output: False

#Less than or equal to

print(2 <= 3)  # Output: True

